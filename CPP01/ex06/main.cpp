#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"

/*
**	clang++ -Wall -Werror -Wextra -std=c++98 -o main main.cpp Weapon.cpp HumanA.cpp HumanB.cpp && ./main
*/

int			main( void )
{
	{
		Weapon	club = Weapon("crude spiked club");

		HumanA	bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}

	{
		Weapon		club = Weapon("crude spiked club");

		HumanB		jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	return (0);
}